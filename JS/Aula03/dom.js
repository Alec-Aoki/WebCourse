/*
DOM
    Só 1 elemento:
        getElementById()

    Mais de 1 elemento:
        getElementsByTagName()
        getElementsByClassName()
        getElementsByName()
*/

console.log(document.getElementById("nome_usuario")) // Elemento específico
console.log(document.getElementsByTagName("input")) // Coleção
console.log(document.getElementsByClassName("campo_texto")) // Coleção
console.log(document.getElementsByName("nome")) // NodeLiist